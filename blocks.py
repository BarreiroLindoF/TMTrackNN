def one_hot_bid(bid):
    arr = [0] * len(BLOCKS)
    if bid != 0:
        arr[bid - 1] = 1
    return arr

def one_hot_pos(p, base=0):
    arr = [0] * 32
    idx = p - base
    arr[idx] = 1
    return arr

def one_hot_rotation(r):
    arr = [0] * 4
    arr[r] = 1
    return arr

def pad_block_sequence(seq, maxlen):
    pad = [EMPTY_BLOCK] * (maxlen - len(seq))
    return pad + seq

def get_block_name(bid):
    for name, _bid in BLOCKS.items():
        if _bid == bid:
            return name
    return None

